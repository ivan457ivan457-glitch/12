
import { GoogleGenAI } from "@google/genai";

const ai = new GoogleGenAI({ apiKey: process.env.API_KEY || "" });

export const getWeddingAdvice = async (userMessage: string) => {
  try {
    const response = await ai.models.generateContent({
      model: 'gemini-3-flash-preview',
      contents: userMessage,
      config: {
        systemInstruction: `Вы — вежливый и романтичный ассистент на свадьбе Антона и Татьяны. 
        Дата события: 24 марта 2026 года. 
        Локации: 
        1. ЗАГС: г. Санкт-Петербург, Дворец Бракосочетания 1, ул. Английская набережная д.28. Сбор в 15:20, начало в 15:40. В ЗАГСе гостей встретит организатор.
        2. Банкет: Ресторан Nevesomost, ул. Большой П.С. Проспект 37 (через отель «Введенский»). Начало в 18:00, завершение в 21:00.
        
        Важные правила:
        - Дресс-код: Тщательный Total Black (черный классический стиль). Избегать ярких цветов.
        - Подарки: Вместо цветов пара будет рада вкладу в семейный бюджет.
        - Атмосфера: Просьба воздержаться от криков «Горько!», свадьба камерная и уютная.
        - Организатор: Елизавета 8 (914) 911-24-08 (по всем вопросам в рабочее время).
        
        Отвечайте гостям тепло и лаконично. Если спрашивают про еду — будет выбор мясо/рыба и учет аллергий.`,
      },
    });

    return response.text || "Извините, я сейчас не могу ответить. Пожалуйста, обратитесь к нашему организатору Елизавете!";
  } catch (error) {
    console.error("Gemini Error:", error);
    return "Произошла ошибка при связи с ассистентом.";
  }
};
